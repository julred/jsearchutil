package jsearchutil;

import java.io.IOException;
import java.util.logging.Level;
import java.util.logging.Logger;
import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.scene.image.Image;
import javafx.stage.Stage;
import javafx.stage.StageStyle;
import javax.imageio.ImageIO;


public class JSearchUtil extends Application {
    
    @Override
    public void start(Stage stage) throws Exception {
        Parent root = FXMLLoader.load(getClass().getResource("FXMLDocument.fxml"));
        Scene scene = new Scene(root);
        
        stage.getIcons().add(new Image("file:icon.png"));
        
        stage.setScene(scene);
        stage.setTitle(settings.StrWindowTitle + " V."+ settings.StrAppVersion);
        stage.show();
    }
    
    
    @Override
    public void stop(){
        try {
            System.out.println("Program will be closed");
            // Dateien speichern etc.
            handleFile.saveIniFile();
        } catch (IOException ex) {
            Logger.getLogger(JSearchUtil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        launch(args);
    }
    
}
